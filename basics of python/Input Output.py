
"""
input() function allows to get information from user
print() function allows to get output that is displayed in terminal(\n to advance
one line)
"""

# Example of input and print the inserted value

test = input(" What's your name?")
print("Your name is: ", test)

# Example using \n - it will move us on the line below

test = input(" What's your name?\n")
print("Your name is: ", test)
